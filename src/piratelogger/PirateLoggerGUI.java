    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here: EDIT
        if (jPanel1.isVisible()) {
            cancelEdit();
        } else {
            jPanel1.setVisible(true);
            current = PirateLogger.getEntry(jList1.getSelectedIndex());
            jTextField1.setText(current.getTheirCallsign());
            jTextField2.setText(current.getTheirName());
            jTextField3.setText(Integer.toString(current.getTheirDoubloons()));
            jCheckBox1.setSelected(current.isPlank());
            sBand = jComboBox1.getSelectedIndex();
            sMode = jComboBox2.getSelectedIndex();
            switch (current.getBand()) {
                case 15:
                    jComboBox1.setSelectedIndex(0);
                    break;
                case 20:
                    jComboBox1.setSelectedIndex(1);
                    break;
                case 40:
                    jComboBox1.setSelectedIndex(2);
                    break;

            }
            jComboBox2.setSelectedIndex(current.isRunning() ? 0 : 1);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

