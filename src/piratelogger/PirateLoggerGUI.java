    private void updateList() {
        ArrayList<PirateLoggerEntry> list = PirateLogger.getEntries();
        String[] stringList = new String[list.size()];
        for (int i = 0; i < stringList.length; i++) {
            stringList[i] = list.get(i).toListString();
        }
        jList1.setListData(stringList);
    }

    private void cancelEdit() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jCheckBox1.setSelected(false);
        current = null;
        jComboBox1.setSelectedIndex(sBand);
        jComboBox2.setSelectedIndex(sMode);
    }

